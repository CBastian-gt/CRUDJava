    public void agregarVendedor(ArrayList vendedores){
                selectVendedor.removeAllItems();
        
        
        
        Iterator iterador = vendedores.iterator();
        while (iterador.hasNext())
        {
            Object unObject = iterador.next();
            Vendedor v = (Vendedor)unObject;
            String unItem = v.toString();
            selectVendedor.addItem(unItem);
        }
        
        selectVendedor.setSelectedIndex(0);
    }
            
